    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        String metodo = null;
        if (rdbTarjeta.isSelected())      metodo = "tarjeta";
        else if (rdbYape.isSelected()) metodo = "yape";
        else if (rdbPaypal.isSelected()) metodo = "paypal";

        if (metodo == null) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Seleccione un método de pago.", "Aviso",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        state.Pedido pedido = tienda.getPedidoActual();
        pedido.setMetodoPago(metodo);

        proxy.CatalogoProxy proxy = new proxy.CatalogoProxy();
        boolean ok = proxy.procesarPago(pedido, metodo);
        if (ok) {
            pedido.avanzar(); // registro_datos_pago → procesado_pago
            pedido.avanzar(); // procesado_pago → datos_envio
            if (menuPadre != null) menuPadre.mostrarEnvio();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "No se pudo procesar el pago. Verifique su estado de sesión.", "Error de pago",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

