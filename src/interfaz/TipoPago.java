    /** Actualiza el total mostrado. Llamado desde Menu.mostrarPago(). */
    public void actualizar() {
        state.Pedido pedido = tienda.getPedidoActual();
        pedido.setDescuentoPuntos(0);
        pedido.setPuntosCanjeados(0);

        clase.Usuario u = tienda.getUsuarioActual();
        double total = pedido.getCarrito().getPrecio();

        if (u != null && u.getPuntos() >= 100) {
            int puntos         = u.getPuntos();
            // Cuántos soles puede descontar (100 pts = S/10)
            int descuentoMax   = (puntos / 100) * 10;
            int descuentoReal  = (int) Math.min(descuentoMax, total);
            int puntosNecesarios = (descuentoReal / 10) * 100;

            int resp = javax.swing.JOptionPane.showConfirmDialog(this,
                "🎁 Tienes " + puntos + " puntos de fidelidad.\n" +
                "Puedes descontar S/ " + descuentoReal + ".00 usando " + puntosNecesarios + " puntos.\n\n" +
                "¿Quieres aplicar el descuento ahora?",
                "Canjear puntos", javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.QUESTION_MESSAGE);

            if (resp == javax.swing.JOptionPane.YES_OPTION) {
                pedido.setDescuentoPuntos(descuentoReal);
                pedido.setPuntosCanjeados(puntosNecesarios);
                total -= descuentoReal;
            }
        }

        jLabel2.setText("TOTAL A PAGAR: S/ " + String.format("%.2f", total));
        grupoMetodo.clearSelection();
    }

