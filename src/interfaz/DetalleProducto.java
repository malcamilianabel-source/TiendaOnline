    private void btnAgregarCarroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarCarroActionPerformed
        if (articuloActual == null) return;
        if (articuloActual.getStock() <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Este producto está agotado.", "Sin stock",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Verificar permiso (replica lógica del proxy)
        state.Pedido pedido = tienda.getPedidoActual();
        String estado = pedido.getNombreEstado();
        if (estado.equals("sin_registro") || estado.equals("pendiente_registro")) {
            pedido.setEstado(new state.PendienteRegistro());
            if (menuPadre != null) menuPadre.mostrarLogin("DETALLE");
            return;
        }

        Producto producto = articuloActual;
        if (jCheckBox1.isSelected()) producto = new Garantia(producto);
        if (jCheckBox2.isSelected()) producto = new EnvioExpress(producto);

        pedido.getCarrito().agregar(producto);
        if (menuPadre != null) menuPadre.actualizarCarritoBtn();
        javax.swing.JOptionPane.showMessageDialog(this,
            "\"" + producto.getNombre() + "\" agregado.\nPrecio: S/ "
                + String.format("%.2f", producto.getPrecio()),
            "Agregado al carrito", javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnAgregarCarroActionPerformed

