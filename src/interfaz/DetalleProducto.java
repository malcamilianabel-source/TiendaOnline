    /**
     * Carga el artículo en el panel.
     * Llamado desde Menu.mostrarDetalle(Articulo).
     */
    public void setArticulo(Articulo art) {
        this.articuloActual = art;
        jLabel1.setText(art.getNombre());
        jLabel2.setText("Categoría: " + art.getCategoria());
        jLabel3.setText("Stock: " + art.getStock());

        try {
            String nombreImg = art.getNombre().replace("\"", "");
            java.net.URL url = getClass().getResource("/imagenes/productos/" + nombreImg + ".jpg");
            if (url != null) {
                java.awt.Image img = new javax.swing.ImageIcon(url).getImage()
                    .getScaledInstance(200, 200, java.awt.Image.SCALE_SMOOTH);
                jLabel4.setIcon(new javax.swing.ImageIcon(img));
                jLabel4.setText("");
            } else {
                jLabel4.setIcon(null);
                jLabel4.setText("[ sin imagen ]");
            }
        } catch (Exception e) {
            jLabel4.setIcon(null);
            jLabel4.setText("[ sin imagen ]");
        }
        jLabel4.setHorizontalAlignment(javax.swing.SwingConstants.CENTER);

        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        if (art.getStock() <= 0) {
            btnAgregarCarro.setText("AGOTADO");
            btnAgregarCarro.setEnabled(false);
            jLabel3.setForeground(java.awt.Color.RED);
        } else {
            btnAgregarCarro.setText("Agregar al carrito");
            btnAgregarCarro.setEnabled(true);
            jLabel3.setForeground(null);
        }
        actualizarTotal();
    }

    /** Recalcula el total con los decoradores seleccionados */
    private void actualizarTotal() {
        if (articuloActual == null) return;
        double total = articuloActual.getPrecio();
        if (jCheckBox1.isSelected()) total += 200.0; // Garantia
        if (jCheckBox2.isSelected()) total += 30.0;  // Envio Express
        jLabel6.setText("TOTAL: S/ " + String.format("%.2f", total));
    }

