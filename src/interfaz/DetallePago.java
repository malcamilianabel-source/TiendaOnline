    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
                                             

    state.Pedido pedido = tienda.getPedidoActual(); 

// Validación rápida por si no hay pedido activo
if (pedido == null) {
    javax.swing.JOptionPane.showMessageDialog(this, "No hay ningún pedido actual para enviar.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
    return;
}

// 1. Extraemos solo los datos solicitados: cliente, producto y total
String cliente = (pedido.getUsuario() != null) ? pedido.getUsuario().getNombreCompleto() : "Anónimo";  

// CORREGIDO: Se agregó la comilla doble antes de No Especificado
String producto = (pedido.getDireccionEnvio() != null) ? pedido.getDireccionEnvio() : "No Especificado";

// 2. Creamos el JSON limpio solo con esos tres campos
String jsonInputString = String.format(  
    "{\"cliente\": \"%s\", \"producto\": \"%s\", \"total\": %.2f}",  
    cliente,  
    producto,  
    pedido.getMontoTotal()  
); 

// 3. Ejecutamos la petición HTTP en un hilo secundario para no congelar la UI
new Thread(() -> {  
    try {  
        java.net.URL url = java.net.URI.create("https://n8n.buypal.com.pe/webhook/Diseno_Patrones").toURL();
        java.net.HttpURLConnection conn = (java.net.HttpURLConnection) url.openConnection();

        conn.setRequestMethod("POST");  
        conn.setRequestProperty("Content-Type", "application/json; utf-8");  
        conn.setRequestProperty("Accept", "application/json");  
        conn.setDoOutput(true); 

        // Enviamos el JSON  
        try (java.io.OutputStream os = conn.getOutputStream()) {  
            byte[] input = jsonInputString.getBytes("utf-8");  
            os.write(input, 0, input.length);  
        } 

        // Leemos la respuesta de n8n  
        int code = conn.getResponseCode();  
        if (code == 200 || code == 201) {  
            System.out.println("¡Datos enviados con éxito! Código: " + code);  
            javax.swing.SwingUtilities.invokeLater(() -> {
                javax.swing.JOptionPane.showMessageDialog(this, "¡Pedido enviado a Whatsapp exitosamente!");
            });
        } else {  
            System.err.println("[n8n] Error en el servidor. Código: " + code);  
            javax.swing.SwingUtilities.invokeLater(() -> {
                javax.swing.JOptionPane.showMessageDialog(this, "n8n respondió con error. Código: " + code, "Error Servidor", javax.swing.JOptionPane.ERROR_MESSAGE);
            });
        }  
        conn.disconnect(); 

    } catch (Exception e) {  
        System.err.println("[n8n] Error al conectar con el webhook: " + e.getMessage());  
        javax.swing.SwingUtilities.invokeLater(() -> {
            javax.swing.JOptionPane.showMessageDialog(this, "No se pudo conectar con n8n:\n" + e.getMessage(), "Error de Conexión", javax.swing.JOptionPane.ERROR_MESSAGE);
        });
    }  
}).start();

    }//GEN-LAST:event_jButton2ActionPerformed

