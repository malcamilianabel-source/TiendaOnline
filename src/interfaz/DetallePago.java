    /** Carga todos los datos del pedido confirmado. Llamado desde Menu.mostrarConfirmacion(). */
    public void actualizar() {
        state.Pedido pedido = tienda.getPedidoActual();

        // Capturar valores ANTES de resetear el pedido
        double subtotal     = pedido.getCarrito().getPrecio();
        int    descuento    = pedido.getDescuentoPuntos();
        double total        = pedido.getMontoTotal();
        int    ptsCanjeados = pedido.getPuntosCanjeados();
        int    ptsGanados   = (int) total;

        clase.Usuario u = pedido.getUsuario();
        jLabel2.setText("Cliente: " + (u != null ? u.getNombreCompleto() : "—"));


        String dir  = pedido.getDireccionEnvio() != null ? pedido.getDireccionEnvio() : "-";
        String tel  = pedido.getNumero() != null ? pedido.getNumero() : "-";
        String fecha;
        try {
            java.time.LocalDate ld = java.time.LocalDate.parse(pedido.getFechaEstimadaEntrega());
            fecha = ld.format(java.time.format.DateTimeFormatter.ofPattern("dd-MM-yyyy"));
        } catch (Exception ex) {
            fecha = pedido.getFechaEstimadaEntrega() != null ? pedido.getFechaEstimadaEntrega() : "-";
        }

        // Construir boleta como texto plano con fuente monoespaciada
        StringBuilder sb = new StringBuilder();
        sb.append("======================================================\n");
        sb.append(String.format("%-22s %s%n", "PRODUCTO", "PRECIO"));
        sb.append("--------------------------------\n");
        for (clase.Producto p : pedido.getCarrito().getItems()) {
            String nom = p.getNombre().length() > 21
                ? p.getNombre().substring(0, 21) + "." : p.getNombre();
            sb.append(String.format("%-22s S/ %.2f%n", nom, p.getPrecio()));
        }
        sb.append("------------------------------------------------------\n");
        sb.append(String.format("%-22s S/ %.2f%n", "Subtotal:", subtotal));
        if (descuento > 0) {
            sb.append(String.format("%-22s -S/ %.2f%n", "Descuento puntos:", (double) descuento));
        }
        sb.append(String.format("%-22s S/ %.2f%n", "TOTAL:", total));
        if (descuento > 0) {
            sb.append(String.format("%-22s -%d pts%n", "Pts canjeados:", ptsCanjeados));
        }
        sb.append(String.format("%-22s +%d pts%n", "Pts ganados:", ptsGanados));
        sb.append("------------------------------------------------------\n");
        sb.append(String.format("%-22s %s%n", "Direccion:", dir));
        sb.append(String.format("%-22s %s%n", "Entrega estimada:", fecha));
        sb.append(String.format("%-22s %s%n", "Telefono:", tel));
        sb.append("========================================================\n");

        txtBoleta.setContentType("text/plain");
        txtBoleta.setFont(new java.awt.Font("Monospaced", java.awt.Font.PLAIN, 12));
        txtBoleta.setForeground(java.awt.Color.BLACK);
        txtBoleta.setBackground(java.awt.Color.WHITE);
        txtBoleta.setText(sb.toString());
        txtBoleta.setEditable(false);
        txtBoleta.setCaretPosition(0);

        // Pedido completado — resetear DESPUÉS de capturar todos los datos
        tienda.nuevoPedido();
        if (menuPadre != null) menuPadre.actualizarCarritoBtn();
    }

